#include "3-calc.h"

/**
 * op_add -  returns the sum of a and b
 * @a: parameter to add
 * @b: second parameter to add with
 * Return: returns the sum of a and b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub -  returns the difference of a and b
 * @a: parameter to subtract
 * @b: second parameter to subtract with
 * Return: returns the difference of a and b
 */

int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul -  returns the multiplication of a and b
 * @a: parameter to multiplies
 * @b: second parameter to multiplies with
 * Return: returns the multiplication of a and b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div -  returns the division of a and b
 * @a: parameter to divide
 * @b: second parameter to divide with
 * Return: returns the division of a and b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod -  returns the remainder of division of a and b
 * @a: parameter to divide
 * @b: second parameter to divide with
 * Return: returns the remainder of division of a and b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
